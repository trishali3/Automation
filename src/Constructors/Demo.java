package Constructors;

public class Demo {
public static void main(String[] args) {
	//implicit casting
	double d=10;
	System.out.println(d);
	//explicit casting
	int i=(int)10.5;//lossy conversion >>>data loss takes place
	System.out.println(i);
	float f=(float)6;
	System.out.println(f);
	
}

}
