package Conditional_statements;

public class Else_ifladder {
public static void main(String[] args) {
	int marks=34;
if(marks>=65)	{
	System.out.println("pass in 1st class with distinction");
	
}
else if(marks>=55){
	System.out.println("passed in 1st class");
}
else if(marks>=45) {
	System.out.println("passed in 2nd class");
}
else if(marks>=35) {
	System.out.println("passed in 3rd class");
}
else {
	System.out.println("successfully failed");
}
	
	
}	

}
