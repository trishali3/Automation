package Conditional_statements;

public class Switch {
public static void main(String[] args) {
switch("blue"){
case "GREEN":{
		System.out.println("Favorite color is green");
		break;
	}
case"BLUE":{
	System.out.println("Favourite color is blue");
	break;
}
case"RED":{
	System.out.println("favourite color is red");
	break;
}
case"WHITE":{
	System.out.println("favourite color is white");
	break;
}
default:{
	System.out.println("No color");
}
}

	
}	
	

}
